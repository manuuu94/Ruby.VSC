# \n to create a new line to a puts, \t creates an indent (sangria) : these are called a escape character. 
puts "hello my name\nis manu"
puts "\thello my name is manu"
# \" \" to include double quotes in a string
puts "hello my name is \"manu\" "   #hello my name is "manu"
puts 'hello my name is \'manu\' '
puts "hello my name is 'manu'"
puts 'hello my name is "manu"'

